# Type Conversion in Python
a = 10          # Integer
b = 5.5         # Float

sum = a + b    # Implicit conversion from int to float
print("Sum (Implicit Conversion):", sum)
print("Type of sum:", type(sum))

# Type casting (Explicit conversion)
# 
a = int("2") # Converting string to integer
b = 4.54 # Float

print(type(a)) #printing the type of a variable
print(a+b) #adding the value of a and b variable

