# Aritamatic Operators
a = 10
b = 5
sum = a + b  # Addition operator
print("Sum:", sum)
diff = a - b  # Subtraction operator
multi = a * b  # Multiplication operator
div = a / b  # Division operator
mod = a % b  # Modulus operator
exp = a ** b  # Exponentiation operator
print("Difference:", diff)
print("Multiplication:", multi)     
print("Division:", div)
print("Modulus:", mod)
print("Exponentiation:", exp)

# Comparison Operators or Relational Operators
x = 10  
y = 20
print( x == y)  # Equal to operator
print(x != y)  # Not equal to operator
print(x > y)  # Greater than operator   
print(x < y)  # Less than operator
print(x >= y)  # Greater than or equal to operator  
print(x <= y)  # Less than or equal to operator

# Assignment Operators
num = 10  # Simple assignment operator
num = num + 5  # Addition assignment operator
num += 5 # Equivalent to num = num + 5
print( num)

